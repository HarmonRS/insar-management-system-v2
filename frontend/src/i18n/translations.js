const ZH_TO_EN = [...TRANSLATION_PAIRS].sort((a, b) => b.zh.length - a.zh.length);
const EN_TO_ZH = [...TRANSLATION_PAIRS].sort((a, b) => b.en.length - a.en.length);

const EXCEPTION_HINT_PATTERNS = [
  /\btraceback\b/i,
  /\b(?:[A-Za-z_]\w*)(?:Error|Exception)\b/,
  /\bNoneType\b/,
  /\bobject is not callable\b/i,
  /File "[^"]+", line \d+/,
  /\bat\s+\S+\s+\([^)]*:\d+:\d+\)/,
  /\bASGI\b/,
  /\buvicorn\b/i,
  /\bstatus_code\b/i,
  /\bSQLSTATE\b/i,
];

const escapeForRegExp = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

const shouldBypassTranslation = (text) => EXCEPTION_HINT_PATTERNS.some((pattern) => pattern.test(text));

const replaceAllPairs = (text, pairs, fromKey, toKey) => {
  let output = text;
  pairs.forEach((item) => {
    const source = item[fromKey];
    const target = item[toKey];
    if (!source) return;
    output = output.replace(new RegExp(escapeForRegExp(source), 'g'), target);
  });
  return output;
};

export const translateText = (text, language) => {
  if (typeof text !== 'string' || !text) return text;
  if (shouldBypassTranslation(text)) return text;
  if (language === 'en') {
    return replaceAllPairs(text, ZH_TO_EN, 'zh', 'en');
  }
  return replaceAllPairs(text, EN_TO_ZH, 'en', 'zh');
};

export const SUPPORTED_LANGUAGES = ['zh', 'en'];
